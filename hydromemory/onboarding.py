# Wizard
# ---------------------------------------------------------------------------


def run_wizard(
    *,
    cwd: Path,
    force: bool = False,
    non_interactive: bool = False,
    preset: str | None = None,
    smoke_test: bool = True,
    stream_in=None,
    stream_out=None,
) -> int:
    """Run the wizard inside ``cwd``. Returns a process exit code.

    The wizard is deterministic given (`cwd`, `stream_in`, `preset`,
    `non_interactive`), which keeps it testable end-to-end without monkeying
    with the global terminal.
    """

    stream_in = stream_in if stream_in is not None else sys.stdin
    stream_out = stream_out if stream_out is not None else sys.stdout

    toml_path = cwd / CONFIG_FILENAME
    env_path = cwd / ENV_FILENAME
    gitignore_path = cwd / GITIGNORE_FILENAME

    defaults = load_toml_defaults(toml_path)
    if preset:
        if preset not in PRESETS:
            stream_out.write(f"error: unknown preset {preset!r}; choose from {sorted(PRESETS)}\n")
            return 2
        for k, v in PRESETS[preset].items():
            setattr(defaults, k, v)

    existed = toml_path.exists()
    if existed and not force and not non_interactive:
        stream_out.write(
            f"Found existing {CONFIG_FILENAME}. Re-running will use its values as defaults.\n"
            "Press Ctrl-C to abort, or enter to continue.\n"
        )
        # The continue-prompt reads one line so a piped test can opt past it.
        stream_in.readline()

    stream_out.write("\nHydroMemory init — droplet engine setup\n\n")

    answers = InitAnswers(**{k: getattr(defaults, k) for k in InitAnswers.__dataclass_fields__})

    # 1. Project name
    answers.project_name = _ask(
        "Project name",
        defaults.project_name,
        stream_in=stream_in,
        stream_out=stream_out,
        non_interactive=non_interactive,
    )

    # 2. Storage
    stream_out.write(
        "\nWhere should droplets live?\n"
        "  sqlite              local file (default, only shipped backend)\n"
        "  postgres            pgvector — experimental, not yet implemented\n"
    )
    answers.storage_backend = _ask(
        "Backend",
        defaults.storage_backend,
        choices=["sqlite", "postgres"],
        stream_in=stream_in,
        stream_out=stream_out,
        non_interactive=non_interactive,
    )
    if answers.storage_backend == "postgres":
        stream_out.write(
            "  note: postgres backend is reserved but not implemented; "
            "falling back to sqlite for now.\n"
        )
        answers.storage_backend = "sqlite"
    answers.db_path = _ask(
        "SQLite path",
        defaults.db_path,
        stream_in=stream_in,
        stream_out=stream_out,
        non_interactive=non_interactive,
    )

    # 3. Intelligence backend
    stream_out.write(
        "\nIntelligence backend (text ops, summaries, classifiers):\n"
        "  stub                deterministic, offline, no key (recommended)\n"
        "  claude              uses ANTHROPIC_API_KEY\n"
    )
    answers.intelligence_backend = _ask(
        "Backend",
        defaults.intelligence_backend,
        choices=["stub", "claude"],
        stream_in=stream_in,
        stream_out=stream_out,
        non_interactive=non_interactive,
    )
    if answers.intelligence_backend == "claude":
        env_key = os.environ.get("ANTHROPIC_API_KEY", "")
        prompt_default = env_key or defaults.anthropic_api_key or "(skip — set later)"
        raw = _ask(
            "ANTHROPIC_API_KEY (enter to skip)",
            prompt_default,
            stream_in=stream_in,
            stream_out=stream_out,
            non_interactive=non_interactive,
        )
        answers.anthropic_api_key = "" if raw == prompt_default and raw.startswith("(skip") else raw

    # 4. Embeddings
    stream_out.write(
        "\nEmbeddings:\n"
        "  stub                deterministic vectors (dim=256)\n"
        "  local               MiniLM (installs `.[local]`, dim=384, offline)\n"
        "  claude              uses ANTHROPIC_API_KEY\n"
    )
    answers.embedding_backend = _ask(
        "Embedding source",
        defaults.embedding_backend,
        choices=["stub", "local", "claude"],
        stream_in=stream_in,
        stream_out=stream_out,
        non_interactive=non_interactive,
    )
    if answers.embedding_backend == "local":
        answers.vector_dim = 384
        answers.extras_to_install.append("local")
    elif answers.embedding_backend == "stub":
        answers.vector_dim = 256

    # 5. Vector index
    stream_out.write(
        "\nVector index:\n"
        "  brute               exact, fine up to ~50k droplets\n"
        "  ann                 hnswlib (installs `.[ann]`)\n"
    )
    answers.vector_backend = _ask(
        "Index",
        defaults.vector_backend,
        choices=["brute", "ann"],
        stream_in=stream_in,
        stream_out=stream_out,
        non_interactive=non_interactive,
    )
    if answers.vector_backend == "ann":
        answers.extras_to_install.append("ann")

    # 6. Vault
    answers.vault_enabled = _ask_bool(
        "\nEnable encrypted user vault?",
        defaults.vault_enabled,
        stream_in=stream_in,
        stream_out=stream_out,
        non_interactive=non_interactive,
    )
    if answers.vault_enabled:
        generate = _ask_bool(
            "  generate a passphrase?",
            True,
            stream_in=stream_in,
            stream_out=stream_out,
            non_interactive=non_interactive,
        )
        if generate:
            answers.vault_key = secrets.token_urlsafe(32)
            stream_out.write(
                "  generated vault key (will be written to .env — keep it safe):\n"
                f"    {answers.vault_key}\n"
            )
        else:
            answers.vault_key = _ask(
                "  paste vault passphrase",
                "",
                stream_in=stream_in,
                stream_out=stream_out,
                non_interactive=non_interactive,
            )

    # 7. Identity / permission default scope
    stream_out.write("\nDefault identity (used when absorb/recall is called without flags):\n")
    answers.default_agent = _ask(
        "  agent name",
        defaults.default_agent,
        stream_in=stream_in,
        stream_out=stream_out,
        non_interactive=non_interactive,
    )
    answers.default_trust = _ask(
        "  trust level",
        defaults.default_trust,
        choices=["session", "approved", "high_trust"],
        stream_in=stream_in,
        stream_out=stream_out,
        non_interactive=non_interactive,
    )
    answers.default_reservoir = _ask(
        "  default reservoir for new droplets",
        defaults.default_reservoir,
        choices=["working_stream", "surface", "groundwater", "glacier"],
        stream_in=stream_in,
        stream_out=stream_out,
        non_interactive=non_interactive,
    )

    # 8. Cycle tick
    cycle_raw = _ask(
        "\nCycle tick interval (seconds)",
        str(defaults.cycle_tick_seconds),
        stream_in=stream_in,
        stream_out=stream_out,
        non_interactive=non_interactive,
    )
    try:
        answers.cycle_tick_seconds = float(cycle_raw)
    except ValueError:
        stream_out.write(
            f"  invalid number {cycle_raw!r}, using {defaults.cycle_tick_seconds}\n"
        )
        answers.cycle_tick_seconds = defaults.cycle_tick_seconds

    # ----------------- write -----------------
    toml_text = _render_toml(answers)
    toml_path.write_text(toml_text)
    stream_out.write(f"\n  wrote {toml_path.name}\n")

    env_existing = env_path.read_text() if env_path.exists() else ""
    env_text = merge_env(env_existing, _env_pairs(answers))
    env_path.write_text(env_text)
    try:
        env_path.chmod(0o600)
    except OSError:
        pass
    stream_out.write(f"  wrote {env_path.name} (chmod 600)\n")

    gi_existing = gitignore_path.read_text() if gitignore_path.exists() else ""
    gi_text = merge_gitignore(gi_existing)
    if gi_text != gi_existing:
        gitignore_path.write_text(gi_text)
        stream_out.write(f"  updated {gitignore_path.name}\n")

    if answers.extras_to_install:
        extras = ",".join(sorted(set(answers.extras_to_install)))
        stream_out.write(
            f"\nThis configuration needs extras: pip install -e '.[{extras}]'\n"
        )

    if smoke_test:
        stream_out.write("\nSmoke test: running example A in an in-memory engine...\n")
        try:
            from hydromemory.examples import run_example

            result = run_example("A")
            facts = len(result) if isinstance(result, dict) else "?"
            stream_out.write(f"  ok ({facts} fact(s))\n")
        except Exception as exc:  # pragma: no cover - defensive
            stream_out.write(f"  smoke test failed: {exc}\n")

    stream_out.write(
        "\nNext:\n"
        "  hydromem run-example A\n"
        "  hydromem absorb --content 'my first droplet' --source conversation\n"
        "  hydromem recall 'first droplet'\n"
    )
    return 0


def add_init_subparser(sub: argparse._SubParsersAction) -> None:
    p = sub.add_parser("init", help="interactive setup wizard (writes hydromemory.toml + .env)")
    p.add_argument("--force", action="store_true", help="re-ask every step even if hydromemory.toml exists")
    p.add_argument(
        "--non-interactive",
        action="store_true",
        help="take all defaults (or preset values) without prompting",
    )
    p.add_argument("--preset", choices=sorted(PRESETS), help="apply a preset before prompting")
    p.add_argument(
        "--no-smoke-test",
        action="store_true",
        help="skip the post-init run_example('A') smoke test",
    )


def run_init_cli(args: argparse.Namespace) -> int:
    return run_wizard(
        cwd=Path.cwd(),
        force=getattr(args, "force", False),
        non_interactive=getattr(args, "non_interactive", False),
        preset=getattr(args, "preset", None),
        smoke_test=not getattr(args, "no_smoke_test", False),
    )
